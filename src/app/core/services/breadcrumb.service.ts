  constructor(private router: Router) {
    this.router.events
      .pipe(filter(event => event instanceof NavigationEnd))
      .subscribe(() => {
        this.updateBreadcrumbs();
      });
  }

  private updateBreadcrumbs(): void {
    const urlSegments = this.router.url.split('/').filter(segment => segment && segment !== '');
    const homeConfig = this.routeConfig[''] || { label: 'Home', iconPath: 'M3 12l2-2m0 0l7-7 7 7M5 10v10a1 1 0 001 1h3m10-11l2 2m-2-2v10a1 1 0 01-1 1h-3m-6 0a1 1 0 001-1v-4a1 1 0 011-1h2a1 1 0 011 1v4a1 1 0 001 1m-6 0h6' };

    const breadcrumbs: Breadcrumb[] = [
      { label: 'Home', url: '/dashboard', iconPath: homeConfig.iconPath }
    ];

    let currentUrl = '';
    urlSegments.forEach((segment, index) => {
      // Skip numeric IDs in breadcrumbs
      if (!isNaN(Number(segment))) {
        return;
      }

      currentUrl += `/${segment}`;
      const config = this.routeConfig[segment];
      const label = config?.label || this.capitalize(segment);
      const iconPath = config?.iconPath;

      // Only add if not the last segment or if it's a meaningful route
      if (index < urlSegments.length - 1 || segment !== 'dashboard') {
        breadcrumbs.push({
          label,
          url: currentUrl,
          iconPath
        });
      }
    });

    this.breadcrumbs.set(breadcrumbs);
  }

  private capitalize(str: string): string {
    return str.charAt(0).toUpperCase() + str.slice(1).replace(/-/g, ' ');
  }

  // Get icon path for a specific route segment
  getIconPath(segment: string): string | undefined {
    return this.routeConfig[segment]?.iconPath;
  }

  setBreadcrumbs(breadcrumbs: Breadcrumb[]): void {
    this.breadcrumbs.set(breadcrumbs);
  }
